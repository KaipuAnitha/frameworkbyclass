package httpBasicRequest;

public class PojoData 
{
	/*
	 * poja follows encapsulation principle
	 * so we can declare data as a private
	 */
	
	
	private String name;
	private String job;
	
	
	
	//Methods
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	
	
	

}
